# latihan satuan temperature

# program konversi celsius ke satuan lain

print("PROGRAM KONVERSI TEMPERATUR")

celcius = float(input('Masukan suhu ke celsius !'))
print("suhu adalah", celcius, "celcius")

# reamur
#4/5 * c

reamur = (4/5) * celcius
print("suhu dalam fahrenheit", reamur, "reamur")

# fahrenheit
fahrenheit = ((9/5) * celcius)
print("suhu dalam fahrenheit", fahrenheit, "fahrenheit")

# kelvin
kelvin = celcius+273
print("suhu dalam kelvin adalah", kelvin, "kelvin")
